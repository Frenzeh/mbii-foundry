package main

// LevelDoc defines documentation for a specific attribute level
type LevelDoc struct {
	Name   string `json:"name"`
	Effect string `json:"effect"`
	FPCost int    `json:"fp_cost,omitempty"`
	Tip    string `json:"tip,omitempty"`
}

// AttributeDef defines a Movie Battles II attribute
type AttributeDef struct {
	ID          string `json:"id"`
	Name        string `json:"name"`
	MaxLevel    int    `json:"max_level"`
	Category    string `json:"category"` // "General", "Weapons", "Force", "Saber", "Class Specific", "Advanced"
	Description string `json:"description"`

	// Rich Documentation
	Overview string              `json:"overview,omitempty"`
	Levels   map[string]LevelDoc `json:"levels,omitempty"` // map[string] because JSON keys are strings
	Tips     []string            `json:"tips,omitempty"`
	Tags     []string            `json:"tags,omitempty"`
}

type ClassDef struct {
	ID          string `json:"id"`
	Name        string `json:"name"`
	Description string `json:"description"`
}

type ClassFlagDef struct {
	ID          string `json:"id"`
	Name        string `json:"name"`
	Description string `json:"description"`
	Overview    string `json:"overview,omitempty"`
}

type SaberStyleDef struct {
	ID          string `json:"id"`
	Name        string `json:"name"`
	Description string `json:"description"`
	Overview    string `json:"overview,omitempty"`
}

type GlossaryDef struct {
	ID          string `json:"id"`
	Name        string `json:"name"`
	Description string `json:"description"`
	Overview    string `json:"overview,omitempty"`
}

